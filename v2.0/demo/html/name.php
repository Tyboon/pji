<h2>Where does the name CARNAC come from ?</h2>

<p>
Carnac is an acronym for <i>Comparative Analysis of RNA by Cofolding</i>.
There are at least two other reasons for this name : </p>
<ul>
<li> CARNAC is composed of the words <b>RNA</b>, <b>ARN</b> (ARN means RNA in French) and two cytosines.
</li>
<li> The name is also a reference to the pre-historical <b>alignments of Carnac</b>, in the region of Britanny.
Carnac dates back to about 4,500 BC, and it is believed to be the biggest megalithic site in the world.
 More than 3000 menhirs stand here.   </li>                                      
</ul>


